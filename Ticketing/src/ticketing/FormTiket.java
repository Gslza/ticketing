    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
 try {
        int idWisata = Integer.parseInt(cbWisata.getSelectedItem().toString().split(" - ")[0]);
        int idPengunjung = Integer.parseInt(cbPengunjung.getSelectedItem().toString().split(" - ")[0]);

        String sql = "INSERT INTO tiket VALUES (NULL, ?, ?, ?, ?, ?)";
        Connection con = Koneksi.getConnection();
        PreparedStatement pst = con.prepareStatement(sql);

        pst.setInt(1, idWisata);
        pst.setInt(2, idPengunjung);
        pst.setInt(3, Integer.parseInt(txtJumlah.getText()));
        pst.setInt(4, Integer.parseInt(txtTotal.getText()));
        pst.setDate(5, java.sql.Date.valueOf(txtTanggal.getText()));

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan");
        tampilData();
        kosongkan();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

