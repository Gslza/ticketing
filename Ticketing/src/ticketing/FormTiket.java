    void hitungTotal() {
    try {
        String wisata = cbWisata.getSelectedItem().toString();
        int idWisata = Integer.parseInt(wisata.split(" - ")[0]);

        String sql = "SELECT harga_tiket FROM wisata WHERE id_wisata=?";
        Connection con = Koneksi.getConnection();
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, idWisata);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            int harga = rs.getInt("harga_tiket");
            int jumlah = Integer.parseInt(txtJumlah.getText());
            txtTotal.setText(String.valueOf(harga * jumlah));
        }
    } catch (Exception e) {
        // biarkan kosong saat belum valid
    }
}
void tampilData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Wisata");
    model.addColumn("Pengunjung");
    model.addColumn("Jumlah");
    model.addColumn("Total");
    model.addColumn("Tanggal");
     
    try {
        String sql = "SELECT t.id_tiket, w.nama_wisata, p.nama_pengunjung, "
           + "t.jumlah_tiket, t.total_harga, t.tanggal "
           + "FROM tiket t "
           + "JOIN wisata w ON t.id_wisata = w.id_wisata "
           + "JOIN pengunjung p ON t.id_pengunjung = p.id_pengunjung";
        
        Connection con = Koneksi.getConnection();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt(1),
                rs.getString(2),
                rs.getString(3),
                rs.getInt(4),
                rs.getInt(5),
                rs.getDate(6)
            });
        }
        tblTiket.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}
   void loadWisata() {
    try {
        String sql = "SELECT * FROM wisata";
        Connection con = Koneksi.getConnection();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(sql);

        cbWisata.removeAllItems();
        while (rs.next()) {
            cbWisata.addItem(rs.getInt("id_wisata") + " - " + rs.getString("nama_wisata"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}
void loadPengunjung() {
    try {
        String sql = "SELECT * FROM pengunjung";
        Connection con = Koneksi.getConnection();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(sql);

        cbPengunjung.removeAllItems();
        while (rs.next()) {
            cbPengunjung.addItem(rs.getInt("id_pengunjung") + " - " + rs.getString("nama_pengunjung"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}
